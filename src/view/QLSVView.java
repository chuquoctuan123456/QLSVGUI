    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            int maThiSinh = Integer.valueOf(jTextFieldMaThiSinh2.getText());
            String hoVaTen = jTextFieldHoVaTen1.getText();
            int queQuan = jComboBoxQueQuan1.getSelectedIndex();
            Tinh tinh = Tinh.getTinhById(queQuan - 1);
            String date = jTextFieldNgaySinh.getText();
            SimpleDateFormat sdfInput = new SimpleDateFormat("dd/MM/yyyy");
            Date ngaySinhDate = sdfInput.parse(date);

            boolean gioiTinh = true;

            if (jRadioButtonGioiTinhNam.isSelected()) {
                gioiTinh = true;
            } else if (jRadioButtonGioiTinhNu.isSelected()) {
                gioiTinh = false;
            }

            double mon1 = Double.valueOf(jTextFieldMon1.getText());
            double mon2 = Double.valueOf(jTextFieldMon2.getText());
            double mon3 = Double.valueOf(jTextFieldMon3.getText());

            // Kiểm tra dữ liệu trước khi tạo đối tượng ThiSinh
            if (mon1 < 0 || mon2 < 0 || mon3 < 0) {
                throw new IllegalArgumentException("Điểm không được âm");
            }

            ThiSinh thiSinh = new ThiSinh(maThiSinh, hoVaTen, tinh, ngaySinhDate, gioiTinh, mon1, mon2, mon3);
            int sizeBefore = qlsvModel.size();
            qlsvModel.insert(thiSinh);
            int sizeAfter = qlsvModel.size();

            if (sizeBefore != sizeAfter) {
                addTable();
                xoaForm();
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhập sai định dạng số của điểm .");
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ.");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
