    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
        int selectionRow = jTable1.getSelectedRow();
        if(selectionRow >= 0){
            String studentID = jTable1.getValueAt(selectionRow ,1)+"";
            for (int i = 0; i < qlsvModel.size() ;i++) {
                ThiSinh thiSinh = qlsvModel.getDsThiSinh().get(i);
                if(studentID.equals(thiSinh.getMaThiSinh()+"")){
                    jTextFieldMaThiSinh2.setText(thiSinh.getMaThiSinh()+"");
                    jTextFieldHoVaTen1.setText(thiSinh.getTenSinhVien()+"");
                    jComboBoxQueQuan1.setSelectedItem(thiSinh.getQueQuan()+"");
                    Date date = thiSinh.getNgaySinh();
                    SimpleDateFormat sdfOutput = new SimpleDateFormat("dd/MM/yyyy");
                    String ngaySinhString = sdfOutput.format(date);
                    jTextFieldNgaySinh.setText(ngaySinhString);



                    if (thiSinh.getGioiTinh()) {
                        jRadioButtonGioiTinhNam.setSelected(true);
                    } else {
                        jRadioButtonGioiTinhNu.setSelected(true);
                    }
                    jTextFieldMon1.setText(thiSinh.getDiemMon1() +"");
                    jTextFieldMon2.setText(thiSinh.getDiemMon2() +"");
                    jTextFieldMon3.setText(thiSinh.getDiemMon3() +"");
                    break;
                }
            }
        }

    }

    private void jButtonCapNhatActionPerformed(java.awt.event.ActionEvent evt) {
        for (int i = 0; i < qlsvModel.size() ;i++) {
            ThiSinh thiSinh = qlsvModel.getDsThiSinh().get(i);
            if(thiSinh.getMaThiSinh() == Integer.valueOf(jTextFieldMaThiSinh2.getText())){
                thiSinh.setTenSinhVien(jTextFieldHoVaTen1.getText()+"");
                int queQuan = jComboBoxQueQuan1.getSelectedIndex();
                Tinh tinh = Tinh.getTinhById(queQuan - 1);
                thiSinh.setQueQuan(tinh);
                String date = jTextFieldNgaySinh.getText();
                SimpleDateFormat sdfInput = new SimpleDateFormat("dd/MM/yyyy");
                Date ngaySinhDate = null;
                try {
                    ngaySinhDate = sdfInput.parse(date);
                } catch (ParseException e) {
                    // Handle the parsing error, e.g., show a message to the user
                    e.printStackTrace(); // print the stack trace for debugging
                }

                boolean gioiTinh = true;

                if (jRadioButtonGioiTinhNam.isSelected()) {
                    gioiTinh = true;
                } else if (jRadioButtonGioiTinhNu.isSelected()) {
                    gioiTinh = false;

                }
                thiSinh.setNgaySinh(ngaySinhDate);
                thiSinh.setGioiTinh(gioiTinh);
                thiSinh.setDiemMon1(Double.valueOf(jTextFieldMon1.getText()));
                thiSinh.setDiemMon2(Double.valueOf(jTextFieldMon2.getText()));
                thiSinh.setDiemMon3(Double.valueOf(jTextFieldMon3.getText()));
            }

        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        model.setValueAt(jTextFieldHoVaTen1.getText()+"" ,row ,2);
        int queQuan = jComboBoxQueQuan1.getSelectedIndex();
        Tinh tinh = Tinh.getTinhById(queQuan - 1);
        model.setValueAt(tinh.getTenTinh()+"",row ,3);
        model.setValueAt(jTextFieldNgaySinh.getText()+"" ,row ,4);
        boolean gioiTinhNam = jRadioButtonGioiTinhNam.isSelected();
        String gioiTinh = gioiTinhNam ? "Nam" : "Nữ";
        model.setValueAt(gioiTinh, row, 5);
        model.setValueAt(jTextFieldMon1.getText()+"" ,row ,6);
        model.setValueAt(jTextFieldMon2.getText()+"" ,row ,7);
        model.setValueAt(jTextFieldMon3.getText()+"" ,row ,8);
    }
